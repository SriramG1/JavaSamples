package com;
import java.lang.*;
public class DataTypes {
    public static void main(String[] args) {
        byte byteMax = Byte.MAX_VALUE;
        byte byteMin = Byte.MIN_VALUE;
        System.out.println("Maximum number of byte : "+byteMax);
        System.out.println("Minimum number of byte : "+byteMin);

        short shortMax=Short.MAX_VALUE;
        short shortMin=Short.MIN_VALUE;
        System.out.println("Maximum number of short : "+shortMax);
        System.out.println("Minimum number of short : "+shortMin);

        int intMax=Integer.MAX_VALUE;
        int intMin=Integer.MIN_VALUE;
        System.out.println("Maximum number of int : "+intMax);
        System.out.println("Minimum number of int : "+intMin);

        long longMax=Long.MAX_VALUE;
        long longMin=Long.MIN_VALUE;
        System.out.println("Maximum number of long : "+longMax);
        System.out.println("Minimum number of long : "+longMin);

        float floatMax=Float.MAX_VALUE;
        float floatMin=Float.MIN_VALUE;
        System.out.println("Maximum number of float : "+floatMax);
        System.out.println("Minimum number of float : "+floatMin);

        double doubleMax=Double.MAX_VALUE;
        double doubleMin=Double.MIN_VALUE;
        System.out.println("Maximum number of double : "+doubleMax);
        System.out.println("Minimum number of double : "+doubleMin);

    }
}
